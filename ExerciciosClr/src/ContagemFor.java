public class ContagemFor {

    public static void main(String[] args) {
        System.out.println("Contagem com for:");
        for (int i = 1; i <= 100; i++) {
            System.out.println(i);
            System.out.println("Contagem com while:");
            int contador = 1;
            while (contador <= 100) {
                System.out.println(contador);
                contador++;

            }
        }
    }
}
